tupla = ("p", "y", "t", "h", "o", "n",)

print(tupla[2:])  # ("t", "h", "o", "n") "DO INDIDE 2 ATÉ O FINAL"
print(tupla[:2])  # ("p", "y") "DO INDICE 0 ATÉ O 2(O INDICE PASSADO NÃO CONTA)"
print(tupla[1:3])  # ("y", "t") "DO INDICE 1 ATÉ I 3(O INDICE PASSADO NÃO CONTA)"
print(tupla[0:3:2])  # ("p", "t") "DO INCIDE 0, ATÉ O 3, CONTANDO DE 2 EM 2 ELEMENTOS"
print(tupla[::])  # ("p", "y", "t", "h", "o", "n") "ESPELHAMENTO DA TUPLA"
print(tupla[::-1])  # ("n", "o", "h", "t", "y", "p") "REVERSE DA TUPLA"
