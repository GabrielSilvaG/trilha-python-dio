conjunto_a = {1, 2}
conjunto_b = {3, 4}

resultado = conjunto_a.union(conjunto_b) #RECEBE "UNION" DOS CONJUNTOS "A" E "B".
print(resultado)

resultado = conjunto_a | conjunto_b #Também pode usar o operador | para realizar a união de conjuntos:
print(resultado)


#Esse código realiza a união (.union()) de dois conjuntos em Python. A união de conjuntos é uma operação que resulta em um novo conjunto contendo todos os elementos únicos presentes em ambos os conjuntos originais.
