conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

resultado = conjunto_a.intersection(conjunto_b) #RECEBE A INTRESECTION DOS CONJUNTOS "A" E "B"
print(resultado)


#Este código calcula a interseção de dois conjuntos em Python. A interseção de conjuntos é um novo conjunto que contém APENAS os elementos que estão presentes em AMBOS os conjuntos originais.
