numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

print(numeros)  # {1, 2, 3, 4, 5, 6, 7, 8, 9, 0}

numeros.discard(1)
numeros.discard(45)

print(numeros)  # {2, 3, 4, 5, 6, 7, 8, 9, 0}


#O método discard() em Python é utilizado para remover um elemento específico de um conjunto. A grande vantagem do discard() é que ele não gera um erro se o elemento que você tentar remover não estiver presente no conjunto.
