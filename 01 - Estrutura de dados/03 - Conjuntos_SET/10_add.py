sorteio = {1, 23}

sorteio.add(25)  # {1, 23, 25}
print(sorteio)

sorteio.add(42)  # {1, 23, 25, 42}
print(sorteio)

sorteio.add(25)  # {1, 23, 25, 42}
print(sorteio)


#Este código demonstra como utilizar o método add() para INSERIR elementos em um conjunto em Python. Conjuntos são coleções NÃO ORDENADAS de elementos únicos, ou seja, UM MESMO elemento NÃO pode aparecer MAIS DE UMA VEZ dentro de um conjunto.(SÓ VAI SER ADICIONADO, SE ITEM DENTRO NÃO FOR REPETIDO)
